package com.technoetic.xplanner.security.config;

/**
 * The Class SecurityRole.
 */
public class SecurityRole {
    
    /** The name. */
    private String name;

    /**
     * Gets the name.
     *
     * @return the name
     */
    public String getName() {
        return this.name;
    }

    /**
     * Sets the name.
     *
     * @param name
     *            the new name
     */
    public void setName(final String name) {
        this.name = name;
    }
}
